ROUND(USERS_M * 100.0 /SUM(USERS_M) OVER(),2) AS MARKET_SHARE_PCT
FROM COMPETITORS ORDER BY MARKET_SHARE_PCT DESC;

/*Competitive Concentration (HHI)*/
WITH SHARES AS (SELECT USERS_M /SUM(USERS_M) OVER() AS SHARE
FROM COMPETITORS)
SELECT ROUND(SUM(POWER(SHARE,2))*10000,0)
AS HHI_SCORE FROM SHARES;


