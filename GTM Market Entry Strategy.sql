/*State Market Ranking*/
SELECT STATE, POPULATION_M
FROM STATE_DATA
ORDER BY POPULATION_M DESC;

/* Top 5 Launch Markets*/
SELECT STATE,POPULATION_M
FROM STATE_DATA
ORDER BY POPULATION_M DESC
LIMIT 5;

/* State Opportunity Index*/
SELECT STATE, POPULATION_M,
ROUND(POPULATION_M /(SELECT AVG(POPULATION_M)FROM STATE_DATA),2) AS OPPORTUNITY_INDEX
FROM STATE_DATA
ORDER BY OPPORTUNITY_INDEX DESC;

/*Launch Priority Matrix*/
SELECT STATE, POPULATION_M,
ROUND(POPULATION_M /(SELECT AVG(POPULATION_M)FROM STATE_DATA),2) AS OPPORTUNITY_SCORE,
CASE
WHEN POPULATION_M >=100 THEN 'PHASE 1'
WHEN POPULATION_M >=70 THEN 'PHASE 2'
ELSE 'PHASE 3'
END AS LAUNCH_PHASE
FROM STATE_DATA
ORDER BY OPPORTUNITY_SCORE DESC;

/*Revenue Potential by State*/
SELECT STATE,POPULATION_M,
ROUND(POPULATION_M * 0.05,2) AS TARGET_USERS_M,
ROUND(POPULATION_M * 0.05 * 700,0) AS POTENTIAL_REVENUE_MN
FROM STATE_DATA
ORDER BY POTENTIAL_REVENUE_MN DESC;

/*Total Competitors*/
SELECT COUNT(*) AS TOTAL_COMPETITORS
FROM COMPETITORS;

/*Largest Competitor*/
SELECT COMPETITOR, USERS_M
FROM COMPETITORS
ORDER BY USERS_M DESC;

/*Largest Merchant Network*/
SELECT COMPETITOR, MERCHANTS
FROM COMPETITORS
ORDER BY MERCHANTS DESC;

/*BNPL vs EMI Competition*/
SELECT TYPE,
COUNT(*) AS TOTAL_PLAYERS
FROM COMPETITORS
GROUP BY TYPE;

/*Average Competitor User Base*/
SELECT ROUND(AVG(USERS_M),2) AS AVG_USERS_M
FROM COMPETITORS;

/*Competitor Ranking*/
SELECT COMPETITOR,USERS_M,RANK() OVER(ORDER BY USERS_M DESC) AS USER_RANK
FROM COMPETITORS;

/*Market Share Analysis*/
SELECT COMPETITOR,USERS_M,
ROUND(USERS_M *100.0 /SUM(USERS_M) OVER(),2) AS MARKET_SHARE
FROM COMPETITORS
ORDER BY MARKET_SHARE DESC;

/*White Space Analysis*/
SELECT COMPETITOR,USERS_M,MERCHANTS,
ROUND(USERS_M / MERCHANTS,2) AS USERS_PER_MERCHANT
FROM COMPETITORS
ORDER BY USERS_PER_MERCHANT DESC;

/*Competitive Threat Analysis*/
SELECT COMPETITOR,USERS_M,MERCHANTS,
CASE
WHEN USERS_M > 50
THEN 'HIGH THREAT'
WHEN USERS_M > 15
THEN 'MEDIUM THREAT'
ELSE 'LOW THREAT'
END AS COMPETITIVE_THREAT
FROM COMPETITORS
ORDER BY USERS_M DESC;

/* HHI Market Concentration Analysis */
WITH MARKET_SHARE AS (SELECT COMPETITOR,USERS_M,
USERS_M / SUM(USERS_M) OVER() AS SHARE
FROM COMPETITORS)
SELECT ROUND(SUM(POWER(SHARE,2)) * 10000,0) AS HHI_SCORE,
CASE
WHEN ROUND(SUM(POWER(SHARE,2)) * 10000,0) < 1500
THEN 'COMPETITIVE MARKET'
WHEN ROUND(SUM(POWER(SHARE,2)) * 10000,0) BETWEEN 1500 AND 2500
THEN 'MODERATELY CONCENTRATED'
ELSE 'HIGHLY CONCENTRATED'
END AS MARKET_STRUCTURE
FROM MARKET_SHARE;

